const express = require('express');
const router = express.Router();
const{
    getAllUsers,
    getUserById,
    createUser,
    updateUser,
    deleteUser
} = require('../controllers/userController');


// User routes
router.route('/').get(getAllUsers)
    .post(createUser);

router.route('/:id')
    .get(getUserById)
    .put(updateUser);

module.exports = router;